<b>What is AwfarShi</b>
<p>
Awfarshi is an offer aggregation website designed to help Lebanese shoppers find all retail and online ongoing offers in the Lebanese market. We are dedicated to finding offers, promotions and sales to help our users save money and save time searching for the best deals.
</p>

<b>How does Awfarshi Work</b>
<p>
Posting offers on Awfarshi is free, our aim is to bring to our audience the most comprehensive list of Shop based offers, promotions and sales. It is possible to give your offers, sales and promotions more exposure on Awfarshi by featuring your offers in our Featured Section . We do not post user to user offers.
</p>
<b>How to Contact Awfarshi</b>
<p>
If you are a Shop that is currently holding a valid promotion, offer or sale contact us by email at info@awfarshi.com and provide us with the details and we will post your request after validation. If you wish you for your offer to be featured on Awfarshi (I.E. Show in the Featured offers section please contact us for details and prices)
For Advertising on Awfarshi you may also contact info@awfarshi.com
